package com.gracia.no2;

public class ConstructorMaba {
    private String nama;
    private int nim;

    //Constructor
    public ConstructorMaba() {
    }

    //Getter
    public String getNama() {
        return nama;
    }

    public int getNim() {
        return nim;
    }

    //Setter
    public void setNama(String nama) {
        this.nama = nama;
    }

    public void setNim(int nim) {
        this.nim = nim;
    }
}
